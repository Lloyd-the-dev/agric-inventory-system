     private void populateTableFromDatabase(String searchItem) {
         String columns[] = {"Animal", "Number Available"};

        // Connect to the database and retrieve data
        try (Connection con = getConnection();
              PreparedStatement pst = con.prepareStatement("SELECT * FROM livestock WHERE Animal LIKE ?");
                ) {
                    // Set the search parameter using a wildcard
                    pst.setString(1, "%" + searchItem + "%");

                    ResultSet rs = pst.executeQuery();

           // Create a DefaultTableModel with column names
           DefaultTableModel model = new DefaultTableModel(columns, 0);
           

           // Iterate through the result set and add data to the model
           while (rs.next()) {
               String animal = rs.getString("Animal");
               String availableNumber = rs.getString("AvailableNumber");

               // Add a new row to the model
               model.addRow(new String[]{animal, availableNumber});
           }

           // Set the model to the JTable
           jTable1.setModel(model);
           
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int selectedRow = jTable1.getSelectedRow();
                    if (selectedRow != -1) {
                        // Get the data from the selected row
                        String selectedAnimal = (String) jTable1.getValueAt(selectedRow, 0);
                        String selectedAvailableNumber = (String) jTable1.getValueAt(selectedRow, 1);

                        // Update the JTextFields with the selected data
                        jTextField4.setText(selectedAnimal);
                        jTextField5.setText(selectedAvailableNumber);
                        if("1".equals(admin))
                        {
                            jPanel4.setVisible(true);
                        }
                    }
                }
            }
        });

        } catch (SQLException ex) {
            ex.printStackTrace(); // Handle the exception according to your needs
             JOptionPane.showMessageDialog(this, "Error fetching data from the database.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
       
    
